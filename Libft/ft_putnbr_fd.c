#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	number;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	number = ((n % 10) + '0');
	write(fd, &number, 1);
}
