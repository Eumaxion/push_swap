#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*subs;

	if (!s)
		return (0);
	if (ft_strlen(s) < start)
		return (ft_strdup(""));
	if (ft_strlen(s + start) < len)
		len = ft_strlen(s + start);
	subs = malloc((len + 1) * sizeof(char));
	if (subs)
	{
		ft_strlcpy(subs, s + start, len + 1);
		return (subs);
	}
	return (NULL);
}
